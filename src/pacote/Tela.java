    private void jbTotalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbTotalizarActionPerformed
        // TODO add your handling code here:
        double total = 0;
        
        if(carne.isSelected()){
            total += 9;
            sabor = "Carne";
            precoPastel = "R$9";
        } else if(queijo.isSelected()){
            total += 9;
            sabor = "Queijo";
            precoPastel = "R$9";
        } else if(frango.isSelected()){
            total += 8;
            sabor = "Frango";
            precoPastel = "R$8";
        } else if(calabresa.isSelected()){
            total += 7;
            sabor = "Calabresa";
            precoPastel = "R$7";
        } else if(pizza.isSelected()){
            total += 9;
            sabor = "Pizza";
            precoPastel = "R$9";
        } else if(doce.isSelected()){
            total += 6;
            sabor = "Doce";
            precoPastel = "R$6";
        }
        
        if(coquinha.isSelected()){
            total += 5;
            qtdCoquinha = "1 - R$5";
        }
        if(caldo.isSelected()){
            total += 4;
            qtdCaldo = "1 - R$4";
        }
        if(suco.isSelected()){
            total += 7;
            qtdSuco = "1 - R$7";
        }
        if(agua.isSelected()){
            total += 3;
            qtdAgua = "1 - R$3";
        }
        
        String resultadoFinal = "R$" + total;
        resultado.setText(resultadoFinal);
          
        
    }//GEN-LAST:event_jbTotalizarActionPerformed

